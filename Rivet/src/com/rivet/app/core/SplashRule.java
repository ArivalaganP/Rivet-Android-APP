package com.rivet.app.core;

public class SplashRule {
	
	int delayInHours;
	
	public SplashRule(int delayInHours){
		this.delayInHours=delayInHours;
	}

	public int getDelayInHours() {
		return delayInHours;
	}

	public void setDelayInHours(int delayInHours) {
		this.delayInHours = delayInHours;
	}

}
